QUESTION 17
     
    SELECT C.COURSE_NAME,COUNT(E.STUDENT_ID)
    AS TOTAL_STUDENTS
    FROM COURSE C
    INNER JOIN ENROLLMENT E
    ON C.COURSE_ID =E.COURSE_ID
    GROUP BY C.COURSE_NAME;

QUESTION 18
    
 SELECT F.NAME,COUNT(COURSE_ID)
    AS TOTAL_COURSE
    FROM FACULTY F
    INNER JOIN COURSE C
    ON F.FACULTY_ID=C.FACULTY_ID
    GROUP BY  F.NAME;


QUESTION 19
